package org.exoplatform.mongo.exception;

import javax.ws.rs.Produces;
import javax.ws.rs.core.MediaType;
import javax.ws.rs.core.Response;
import javax.ws.rs.ext.ExceptionMapper;
import javax.ws.rs.ext.Provider;

import org.exoplatform.mongo.error.ClientError;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

/**
 * @author <a href="mailto:sondn@exoplatform.com">Ngoc Son Dang</a>
 * @since Jul 13, 2013
 * @version
 * 
 * @tag
 */
@Provider
@Produces(MediaType.APPLICATION_JSON)
public class BaseExceptionMapper implements ExceptionMapper<Exception> {

	private static Logger logger = LoggerFactory.getLogger(BaseExceptionMapper.class);

	/*
	 * (non-Javadoc)
	 * 
	 * @see javax.ws.rs.ext.ExceptionMapper#toResponse(java.lang.Throwable)
	 */
	@Override
	public Response toResponse(Exception exception) {
		logger.error("Service exception handled by BaseExceptionMapper", exception);
		
        return Response.status(ClientError.BAD_REQUEST.code()).type(MediaType.APPLICATION_JSON)
        		.entity(ClientError.BAD_REQUEST.message()).build();
	}
}
